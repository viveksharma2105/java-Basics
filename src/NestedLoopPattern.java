public class NestedLoopPattern {
    public static void main(String[] args) {
        //pattern are generally printed in 2D plane so , we have to use only 2 for loops for pattern printing
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();

        }


    }

}

